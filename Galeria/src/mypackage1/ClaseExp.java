package mypackage1;

public class ClaseExp 
{
  String titulo;
  String descr;
  String fecha_ini;
  String fecha_cierre;
  String id;

  public ClaseExp()
  {
  }

  public String getTitulo()
  {
    return titulo;
  }

  public void setTitulo(String newTitulo)
  {
    titulo = newTitulo;
  }

  public String getDescr()
  {
    return descr;
  }

  public void setDescr(String newDescr)
  {
    descr = newDescr;
  }

  public String getFecha_ini()
  {
    return fecha_ini;
  }

  public void setFecha_ini(String newFecha_ini)
  {
    fecha_ini = newFecha_ini;
  }

  public String getFecha_cierre()
  {
    return fecha_cierre;
  }

  public void setFecha_cierre(String newFecha_cierre)
  {
    fecha_cierre = newFecha_cierre;
  }

  public String getId()
  {
    return id;
  }

  public void setId(String newId)
  {
    id = newId;
  }
}